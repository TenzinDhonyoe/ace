// Renders an ace site.config.json into a single self-contained HTML file.
//
// Layout ported from the hand-tuned BME804 reference:
//   - sticky header with title, progress pill, search, quiz jump, help, theme toggle
//   - shell grid: sidebar (collapsible on mobile) + main
//   - sidebar: groups per section with sub-topic sub-links, active-state tracking
//   - search palette, help overlay, side-backdrop for mobile — DOM lives in HTML,
//     behavior lives in runtime.js inlined as a <script>.

import { validate, siteConfigSchema, widgetTypes } from "@ace/components";
import { runtimeScript } from "./runtime.js";

export function renderSite(config, opts = {}) {
  const result = validate(config, siteConfigSchema);
  if (!result.valid) {
    throw new Error("Invalid site.config.json:\n  " + result.errors.join("\n  "));
  }

  // Pre-flight: every widget type must be in the registry
  for (const section of config.sections) {
    const widgets = collectWidgets(section);
    for (const w of widgets) {
      if (!widgetTypes.includes(w.type)) {
        throw new Error(`Unknown widget type "${w.type}" in section "${section.id}". Known: ${widgetTypes.join(", ")}`);
      }
    }
  }

  // Cache-bust ES module imports so regenerating the site doesn't leave the
  // browser holding the previous module graph. Any unique per-render string
  // works — we use config.meta.generatedAt if present, else current timestamp.
  const cacheBust = encodeURIComponent(config.meta.generatedAt || String(Date.now()));
  const withBust = (url) => url + (url.includes("?") ? "&" : "?") + "v=" + cacheBust;
  const componentsBundleUrl = withBust(opts.componentsBundleUrl || "./ace-components.js");
  const stylesUrl = withBust(opts.stylesUrl || "./ace-styles.css");
  const siteId = config.siteId || slug(config.meta.course + "-" + (config.meta.examDate || ""));
  const metaLine = [escapeHtml(config.meta.course), config.meta.institution ? escapeHtml(config.meta.institution) : "", config.meta.examDate ? `exam ${escapeHtml(config.meta.examDate)}` : ""].filter(Boolean).join(" · ");

  return [
    `<!DOCTYPE html>`,
    `<html lang="en">`,
    `<head>`,
    `  <meta charset="UTF-8">`,
    `  <meta name="viewport" content="width=device-width, initial-scale=1">`,
    `  <title>${escapeHtml(config.meta.title)}</title>`,
    `  <link rel="stylesheet" href="${escapeHtml(stylesUrl)}">`,
    // KaTeX for math rendering — auto-render picks up $...$ (inline) and $$...$$ (display).
    // Via CDN, not vendored — users who regenerate don't need a build step.
    `  <link rel="stylesheet" href="https://cdn.jsdelivr.net/npm/katex@0.16.9/dist/katex.min.css" crossorigin="anonymous">`,
    `  <script defer src="https://cdn.jsdelivr.net/npm/katex@0.16.9/dist/katex.min.js" crossorigin="anonymous"></script>`,
    `  <script defer src="https://cdn.jsdelivr.net/npm/katex@0.16.9/dist/contrib/auto-render.min.js" crossorigin="anonymous" onload="renderMathInElement(document.body, { delimiters: [ {left:'$$',right:'$$',display:true}, {left:'\\\\[',right:'\\\\]',display:true}, {left:'$',right:'$',display:false}, {left:'\\\\(',right:'\\\\)',display:false} ], throwOnError:false, strict:'ignore' });"></script>`,
    `  <style>${renderInlineLayoutCss()}</style>`,
    `</head>`,
    `<body class="ace-root">`,
    `  <header class="ace-top">`,
    `    <div class="ace-top__bar">`,
    `      <button class="ace-menu-btn" id="menuBtn" aria-label="Menu">☰</button>`,
    `      <div class="ace-top__title">${escapeHtml(config.meta.title)}${metaLine ? `<span class="ace-top__sub">${metaLine}</span>` : ""}</div>`,
    `      <div class="ace-top__spacer"></div>`,
    `      <span class="ace-pill" id="progressPill" title="Mastered (click to reset)"><span class="ace-pill__bar"><span id="progressBar"></span></span> <span id="progressTxt"><b>0</b>/0</span></span>`,
    `      <button class="ace-top__btn" id="searchOpen" title="Search (⌘K)"><span>Search</span> <b>⌘K</b></button>`,
    `      <button class="ace-top__btn" id="quizJump" title="Jump to quiz">Quiz</button>`,
    `      <button class="ace-top__btn" id="helpBtn" title="Keyboard shortcuts (?)">?</button>`,
    `      <button class="ace-top__btn" id="themeBtn" title="Toggle theme">☾</button>`,
    `    </div>`,
    `  </header>`,
    `  <div class="ace-side-backdrop" id="sideBackdrop"></div>`,
    `  <div class="ace-shell">`,
    `    <aside class="ace-side" id="side">`,
    config.sections.map((s) => renderSidebarGroup(s)).join("\n"),
    `    </aside>`,
    `    <main id="main" class="ace-main">`,
    ...config.sections.map((section) => renderSection(section)),
    `      <footer class="ace-footer">Generated by <b>ace-review</b> from lecture PDFs. Site ID: <code>${escapeHtml(siteId)}</code>.</footer>`,
    `    </main>`,
    `  </div>`,
    renderPalette(),
    renderHelp(),
    `  <script type="module">`,
    `    import { registry } from "${escapeJs(componentsBundleUrl)}";`,
    `    const siteId = ${JSON.stringify(siteId)};`,
    `    const widgets = ${JSON.stringify(flattenAllWidgets(config))};`,
    `    for (const w of widgets) {`,
    `      const el = document.getElementById(w.mountId);`,
    `      if (!el) continue;`,
    `      const Cls = registry[w.type];`,
    `      if (!Cls) { el.innerHTML = '<div class="ace-error"><strong>Unknown widget: '+w.type+'</strong></div>'; continue; }`,
    `      new Cls().mount(el, w.props, { siteId });`,
    `    }`,
    `    // Re-run KaTeX on newly mounted prose widgets. The CDN auto-render fired at`,
    `    // page onload, before these widgets existed. Wait for KaTeX to be ready.`,
    `    const tryRender = () => {`,
    `      if (typeof renderMathInElement === 'function') {`,
    `        renderMathInElement(document.body, {`,
    `          delimiters: [ {left:'$$',right:'$$',display:true}, {left:'\\\\[',right:'\\\\]',display:true}, {left:'$',right:'$',display:false}, {left:'\\\\(',right:'\\\\)',display:false} ],`,
    `          throwOnError: false, strict: 'ignore'`,
    `        });`,
    `      } else {`,
    `        setTimeout(tryRender, 100);`,
    `      }`,
    `    };`,
    `    tryRender();`,
    `  </script>`,
    `  <script>${runtimeScript()}</script>`,
    `</body>`,
    `</html>`,
  ].join("\n");
}

function renderSidebarGroup(section) {
  const subs = section.subtopics || [];
  const lines = [
    `      <nav class="ace-grp" data-topic="${escapeHtml(section.id)}">`,
    `        <h4><a href="#${escapeHtml(section.id)}">${escapeHtml(section.title)}</a></h4>`,
    ...subs.map((sub) => `        <a href="#${escapeHtml(sub.id)}">${escapeHtml(sub.title)}</a>`),
    `      </nav>`,
  ];
  return lines.join("\n");
}

function renderSection(section) {
  const widgets = section.widgets || [];
  const subtopics = section.subtopics || [];
  const lines = [
    `      <section id="${escapeHtml(section.id)}">`,
    `        <h2>${escapeHtml(section.title)}</h2>`,
    section.summary ? `        <p class="ace-lede">${escapeHtml(section.summary)}</p>` : "",
  ];
  // Flat widgets (if any)
  for (const w of widgets) {
    lines.push(`        <div id="${escapeHtml(mountId(section.id, w.id))}"></div>`);
  }
  // Subtopics (if any)
  for (const sub of subtopics) {
    lines.push(`        <h3 id="${escapeHtml(sub.id)}">${escapeHtml(sub.title)}</h3>`);
    if (sub.summary) lines.push(`        <p class="ace-sub-lede">${escapeHtml(sub.summary)}</p>`);
    for (const w of (sub.widgets || [])) {
      lines.push(`        <div id="${escapeHtml(mountId(section.id, w.id))}"></div>`);
    }
  }
  lines.push(`      </section>`);
  return lines.filter(Boolean).join("\n");
}

