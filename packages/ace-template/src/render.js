// ---- helpers ----

function collectWidgets(section) {
  const widgets = [];
  if (Array.isArray(section.widgets)) widgets.push(...section.widgets);
  if (Array.isArray(section.subtopics)) {
    for (const sub of section.subtopics) {
      if (Array.isArray(sub.widgets)) widgets.push(...sub.widgets);
    }
  }
  return widgets;
}

function flattenAllWidgets(config) {
  const out = [];
  for (const s of config.sections) {
    for (const w of collectWidgets(s)) {
      out.push({ mountId: mountId(s.id, w.id), type: w.type, props: { ...w.props, id: w.id } });
    }
  }
  return out;
}

function mountId(sectionId, widgetId) { return `ace-${sectionId}-${widgetId}`; }
function slug(s) { return String(s).toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "") || "site"; }
function escapeHtml(s) {
  return String(s).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");
}
function escapeJs(s) { return String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"'); }
